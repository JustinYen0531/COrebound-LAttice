/**
 * 建立一個白色外框、黑底實心背景、無模糊光暈、縮放 2 倍放大的單個角色標識圖示
 * @param 角色ID 角色對應的 id
 * @param 星級 1 ~ 3 等級
 * @param 半徑 擺放的極座標半徑
 * @param 圖示顏色 圖示線條專屬顏色（藍/紅/黃，或是純白色）
 * @param 角度 定位角度 (極座標 deg，0度為正上方)
 */
export function 建立單個角色圖徽(
  角色ID: string,
  星級: number,
  半徑: number,
  圖示顏色: string,
  角度: number
): SVGElement {
  const 徽記定位G = document.createElementNS("http://www.w3.org/2000/svg", "g");
  // 利用 transform 直接進行旋轉和半徑偏移
  徽記定位G.setAttribute("transform", `rotate(${角度}) translate(0, ${-半徑})`);

  // 1. 建立黑色實心背景圓圈，直徑放大近兩倍（半徑從 11.5 加大到 21px，剛好 42px 直徑，超高對比遮擋）
  const 背景圓 = document.createElementNS("http://www.w3.org/2000/svg", "circle");
  背景圓.setAttribute("cx", "0");
  背景圓.setAttribute("cy", "0");
  背景圓.setAttribute("r", "21");
  背景圓.setAttribute("fill", "#05060b"); // 貝殼黑色底
  背景圓.setAttribute("stroke", "#ffffff"); // 乾淨明亮的白色外邊框
  背景圓.setAttribute("stroke-width", "2.2");
  背景圓.setAttribute("opacity", "0.95");
  徽記定位G.appendChild(背景圓);

  // 2. 建立縮放兩倍的白描角色向量圖形，無任何光暈濾鏡，確保極致銳利清晰
  const paths = 圖徽路徑資料庫[角色ID.toLowerCase()]?.[星級 - 1] ?? 圖徽路徑資料庫.prism[0];
  
  // 建立縮放 1.85 倍的向量群組，剛好適配 21px 半徑的黑底圓
  const pathG = document.createElementNS("http://www.w3.org/2000/svg", "g");
  pathG.setAttribute("transform", "scale(1.8)");

  paths.forEach((p) => {
    const pathEl = document.createElementNS("http://www.w3.org/2000/svg", "path");
    pathEl.setAttribute("d", p.d);
    pathEl.setAttribute("fill", "none");
    pathEl.setAttribute("stroke", 圖示顏色);
    pathEl.setAttribute("stroke-width", String(p.strokeWidth ?? 1.5));
    pathEl.setAttribute("stroke-linecap", "round");
    pathEl.setAttribute("stroke-linejoin", "round");
    pathG.appendChild(pathEl);
  });
  
  徽記定位G.appendChild(pathG);
  return 徽記定位G;
}
