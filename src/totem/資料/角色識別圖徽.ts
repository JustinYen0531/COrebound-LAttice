/**
 * 建立一個黑底白線、120度均勻分布、帶有實心黑色背景的小徽記元素群組
 * @param 角色ID 角色對應的 id 字串
 * @param 星級 1 ~ 3 星等
 * @param 半徑 放置在圖騰畫布上的圓半徑
 * @param 顏色 圖徽的主線條發光顏色
 * @param 旋轉角度 徽記本身需要被額外偏移的自旋角
 */
export function 建立120度圖徽群組(
  角色ID: string,
  星級: number,
  半徑: number,
  顏色: string,
  旋轉角度: number = 0
): SVGElement {
  const g = document.createElementNS("http://www.w3.org/2000/svg", "g");
  
  // 取得對應角色與星等的 path 列表，若找不到則預設用 Prism
  const paths = 圖徽路徑資料庫[角色ID.toLowerCase()]?.[星級 - 1] ?? 圖徽路徑資料庫.prism[0];
  
  // 均勻分佈在 3 個 120 度的方位上 (0°, 120°, 240°)
  const angles = [0, 120, 240];
  
  angles.forEach((角度) => {
    // 考慮旋轉角度偏移
    const 總角度 = 角度 + 旋轉角度;
    const rad = (總角度 * Math.PI) / 180;
    
    // 計算徽記中心點在直角座標系中的位置 (相對於畫布中心)
    // 這裡我們需要使用外部傳入的 畫布中心，但為了通用性，我們先在 weaving.ts/preview.ts 裡進行 transform
    // 或者，最簡單的做法是直接在 SVG 中建立一個 `<g>`，透過 transform `rotate(總角度)` 然後 `translate(0, -半徑)`
    // 這在 SVG 中極其優雅！
    const 徽記定位G = document.createElementNS("http://www.w3.org/2000/svg", "g");
    // 注意：因為極座標以正上方 (0°) 為起點，所以我們 rotate(總角度)，然後往上方 translate 負半徑！
    // 這樣圖示就會被擺在正圓周上！
    徽記定位G.setAttribute("transform", `rotate(${總角度}) translate(0, ${-半徑})`);
    
    // 1. 建立「中央是空心的，中間像貝殼一樣黑黑的」實心黑底背景圓圈，蓋住背後的萬花筒細線
    const 背景圓 = document.createElementNS("http://www.w3.org/2000/svg", "circle");
    背景圓.setAttribute("cx", "0");
    背景圓.setAttribute("cy", "0");
    背景圓.setAttribute("r", "11.5");
    背景圓.setAttribute("fill", "#05060b"); // 跟隨模擬器底色，貝殼般漆黑
    背景圓.setAttribute("stroke", "#ffffff"); // 白色邊框，確保辨識度
    背景圓.setAttribute("stroke-width", "1.2");
    背景圓.setAttribute("opacity", "0.95");
    徽記定位G.appendChild(背景圓);
    
    // 2. 建立白色的專屬向量符號
    paths.forEach((p) => {
      const pathEl = document.createElementNS("http://www.w3.org/2000/svg", "path");
      pathEl.setAttribute("d", p.d);
      pathEl.setAttribute("fill", "none");
      pathEl.setAttribute("stroke", 顏色); // 點綴上代表職責 (藍/紅/黃) 的璀璨色彩！
      pathEl.setAttribute("stroke-width", String(p.strokeWidth ?? 1.5));
      pathEl.setAttribute("stroke-linecap", "round");
      pathEl.setAttribute("stroke-linejoin", "round");
      pathEl.setAttribute("filter", "url(#glow)"); // 加上微弱的發光
      徽記定位G.appendChild(pathEl);
    });
    
    g.appendChild(徽記定位G);
  });
  
  return g;
}
